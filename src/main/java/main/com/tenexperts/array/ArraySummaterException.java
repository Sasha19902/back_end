package main.com.tenexperts.array;


public class ArraySummaterException extends Exception {

    public ArraySummaterException() {
        super();
    }

    public ArraySummaterException(String message) {
        super(message);
    }

    public ArraySummaterException(String message, Throwable cause) {
        super(message, cause);
    }

    public ArraySummaterException(Throwable cause) {
        super(cause);
    }
}
